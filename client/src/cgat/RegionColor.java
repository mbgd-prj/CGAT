import java.awt.*;

///////////////////////////////////////////////////////////////////////////////
//
public class RegionColor {
    int   from;
    int   to;
    byte  dir;        // 向き（3→5:-1, なし:0, 5→3:1）
    Color color;

    ///////////////////////////////////////////////////////////////////////////
    //
    public RegionColor(int f, int t, byte d) {
        setFrom(f);
        setTo(t);
        setDir(d);
        setColor(Color.black);
    }

    ///////////////////////////////////////////////////////////////////////////
    //
    public RegionColor(int f, int t, byte d, Color c) {
        setFrom(f);
        setTo(t);
        setDir(d);
        setColor(c);
    }

    ///////////////////////////////////////////////////////////////////////////
    //
    public RegionColor(int f, int t, byte d, int c) {
        setFrom(f);
        setTo(t);
        setDir(d);
        setColor(c);
    }

    ///////////////////////////////////////////////////////////////////////////
    //
    public void setFrom(int f) {
        from = f;
    }

    ///////////////////////////////////////////////////////////////////////////
    //
    public void setTo(int t) {
        to = t;
    }

    ///////////////////////////////////////////////////////////////////////////
    //
    public void setDir(byte d) {
        dir = d;
    }

    ///////////////////////////////////////////////////////////////////////////
    //
    public void setColor(Color c) {
        color = c;
    }

    ///////////////////////////////////////////////////////////////////////////
    //
    public void setColor(int c) {
        color = new Color(c);
    }

    ///////////////////////////////////////////////////////////////////////////
    //
    public int getFrom() {
        return(from);
    }

    ///////////////////////////////////////////////////////////////////////////
    //
    public int getTo() {
        return(to);
    }

    ///////////////////////////////////////////////////////////////////////////
    //
    public byte getDir() {
        return(dir);
    }

    ///////////////////////////////////////////////////////////////////////////
    //
    public Color getColor() {
        return(color);
    }






}
