#!/usr/bin/perl -s

###############################################################################
use lib "$ENV{'CGAT_HOME'}/perllib";
require "libCheckPoint.pl";

###############################################################################
if ($0 eq __FILE__) {
    my($sp);

    if ($all) {
        # 全チェックポイントファイルを削除
        #   正規表現の . は、全てのファイルにマッチする
        @ARGV = ('.');
    }

    # チェックポイントファイルの削除
    foreach $sp (@ARGV) {
        &clearCheckPoint($sp);
    }
}

###############################################################################
1;#
